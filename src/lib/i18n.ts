import { writable } from 'svelte/store'

const DEFAULT_LANGUAGE = 'en'
const SUPPORTED_LANGUAGES = ['en', 'sv', 'ja'] as const

export type Language = (typeof SUPPORTED_LANGUAGES)[number]

export const language = writable<Language>(DEFAULT_LANGUAGE)

export function isSupportedLanguage(value: string | null | undefined): value is Language {
	return typeof value === 'string' && SUPPORTED_LANGUAGES.includes(value as Language)
}

export function initLanguage() {
	if (typeof window === 'undefined') return
	const stored = window.localStorage.getItem('language')
	const next = isSupportedLanguage(stored) ? stored : DEFAULT_LANGUAGE
	window.localStorage.setItem('language', next)
	language.set(next)
}

export function setLanguage(next: string) {
	const value = isSupportedLanguage(next) ? next : DEFAULT_LANGUAGE
	if (typeof window !== 'undefined') {
		window.localStorage.setItem('language', value)
	}
	language.set(value)
}

